package Practice.Variablea_AND_Data_Types;

public class swap_two {
    public static void main(String[] args) {
        int x=5;
        int y=8;
        int temp=x;
        x=y;
        y=temp;
        System.out.println("Value of x is "+x);
        System.out.println("Value of y is "+y);
    }
}
